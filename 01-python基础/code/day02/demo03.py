"""
    数据类型
"""

# 1. 整形  int
# 十进制：0123456789  10
a = 500
# 二进制(逢二进一):0  1  10
b = 0b10
# 八进制(逢八进一)：0 1 2 3 4 5 6 7   10  11  12
c = 0o10
# 十六进制：0 1 2 3 --> 9    a(10)  --> f(15)
d = 0x10
print(d)

# type 函数 可以查看变量所指向的对象类型
print(type(a))

# 2. 浮点型
e = 1.23
# 以科学计数法表达
print(0.00000000000000000000005)# 5e-23
print(type(e))


# 3. 字符串 str
name = "张无忌"
f = "100"# 只是字符串不是整数100
print("name")# 打印name,而不是变量.
