# 练习: “我是明教教主张无忌”
# 16:50 上课
str01 = "我是明教教主张无忌"
# 打印第一个字符
print(str01[0])
# 打印最后一个字符
print(str01[-1])# 通过反向索引
print(str01[len(str01)-1])# 通过正向索引
# 打印中间的字符
print(str01[len(str01) // 2])
# 打印前三个字符
print(str01[:3])# 通过反向索引
# 打印后三个字符
print(str01[-3:])# 通过反向索引
# 打印 我教张
print(str01[::3])# 通过反向索引
# 忌张教明我
print(str01[::-2])# 通过反向索引
# ...
print(str01[2:20])# 通过反向索引

